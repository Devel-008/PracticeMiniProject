package practice.student;

public class Student {
    private static int rollno;
    private  static String name;
    private  static String fathername;
    private  static String address;
    private  static String dob;
    private  static float english;
    private  static float hindi;
    private  static float maths;
    private  static float science;
    private  static float social;
    private  static float percentage;

    public static int getRollno() {
        return rollno;
    }

    public static void setRollno(int rollno) {
        Student.rollno = rollno;
    }

    public static String getName() {
        return name;
    }

    public static void setName(String name) {
        Student.name = name;
    }

    public static String getFathername() {
        return fathername;
    }

    public static void setFathername(String fathername) {
        Student.fathername = fathername;
    }

    public static String getAddress() {
        return address;
    }

    public static void setAddress(String address) {
        Student.address = address;
    }

    public static String getDob() {
        return dob;
    }

    public static void setDob(String dob) {
        Student.dob = dob;
    }

    public static float getEnglish() {
        return english;
    }

    public static void setEnglish(float english) {
        Student.english = english;
    }

    public static float getHindi() {
        return hindi;
    }

    public static void setHindi(float hindi) {
        Student.hindi = hindi;
    }

    public static float getMaths() {
        return maths;
    }

    public static void setMaths(float maths) {
        Student.maths = maths;
    }

    public static float getScience() {
        return science;
    }

    public static void setScience(float science) {
        Student.science = science;
    }

    public static float getSocial() {
        return social;
    }

    public static void setSocial(float social) {
        Student.social = social;
    }

    public static float getPercentage() {
        return percentage;
    }

    public static void setPercentage(float percentage) {
        Student.percentage = percentage;
    }

}
